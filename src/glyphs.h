// ─── グリフ座標エンコーディング ─────────────────────────────────
//  data[] は int8_t のストリーム:
//    GLYPH_PENUP x y   → ペンアップ、(x,y) へ移動
//    x           y     → ペンダウン描画 (x != GLYPH_PENUP)
//
//  ワーク座標(mm) = GLYPH_CX + x * GLYPH_SCALE
